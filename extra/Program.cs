// Разобраться с алгоритмом сортировки методом пузырька. Реализовать невозрастающую сортировку.
// [3, 0, 2, 4, -1] -> [4, 3, 2, 0, -1]
// [1,2,2,3,2] -> [3, 2, 2, 2, 1]

int[] a = { -5,5,6,1,14 };
int num;
for (int i = 0; i < a.Length; i++)
{
    for (int j = i + 1; j < a.Length; j++)
    {
        if (a[i] < a[j])
        {
            num = a[i];
            a[i] = a[j];
            a[j] = num;
        }
    }
}
Console.WriteLine(String.Join(", ", a));